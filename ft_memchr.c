#include <stddef.h>
#include <stdio.h>
#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*src;

	src = (const unsigned char *)s;
	while (n--)
	{
		if (*src == c)
			return (c);
		else
			*src++;
	}
	return (0);
}
